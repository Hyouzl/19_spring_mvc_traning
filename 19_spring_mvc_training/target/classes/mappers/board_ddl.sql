CREATE DATABASE SPRING_MVC_TRAINING2;
USE SPRING_MVC_TRAINING2;

CREATE TABLE T_SPRING_BOARD(
	NUM 		INT PRIMARY KEY AUTO_INCREMENT,
    WRITER 		VARCHAR(50),
    EMAIL 		VARCHAR(50),
    SUBJECT 	VARCHAR(50),
    PASSWORD 	VARCHAR(20),
    REG_DATE 	DATE,
    READ_COUNT  INT,
   	CONTENT 	VARCHAR(200)
);


SELECT 
		*
FROM
        T_SPRING_BOARD;